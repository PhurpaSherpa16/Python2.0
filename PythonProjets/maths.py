from math import sqrt as s, pow as p, ceil as c, floor, pi, e
x = s(26)
print("Floor value "+str(floor(x))) #floor is the lower value while rounding figure
print("Ceil value "+str(c(x))) #ceil is the upper value while rounding figure
print("Power of "+ str(p(2,31)))
print("Pi value "+ str(pi))
print("e value "+ str(e))

import math as m
x = m.sqrt(26)
print("Floor value "+str(m.floor(x))) #floor is the lower value while rounding figure
print("Ceil value "+str(m.ceil(x))) #ceil is the upper value while rounding figure
print("Power of "+ str(m.pow(2,31)))
print("Pi value "+ str(m.pi))
print("e value "+ str(m.e))
